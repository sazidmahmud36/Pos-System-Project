export class Employee {
    id:any;
    name:any;
    phone:any;
    email:any;
    password:any;
    role:any;

    constructor(id:any,name:any,phone:any,email:any,password:any,role:any){
        this.id=id;
        this.name=name;
        this.phone=phone;
        this.email=email;
        this.password=password;
        this.role=role;
    
    }

}
